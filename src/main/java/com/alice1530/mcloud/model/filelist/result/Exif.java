
package com.alice1530.mcloud.model.filelist.result;


public class Exif {

     
    private String createTime;
     
    private Object latitude;
     
    private Object localSaveTime;
     
    private Object longitude;

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public Object getLatitude() {
        return latitude;
    }

    public void setLatitude(Object latitude) {
        this.latitude = latitude;
    }

    public Object getLocalSaveTime() {
        return localSaveTime;
    }

    public void setLocalSaveTime(Object localSaveTime) {
        this.localSaveTime = localSaveTime;
    }

    public Object getLongitude() {
        return longitude;
    }

    public void setLongitude(Object longitude) {
        this.longitude = longitude;
    }

}
